package accessModifiers;

public class accessModifiers {

    // default
    // using in same package
    int a;
    int b;

    // private
    // using in relevant java class only
    private String name;
    private String password;

    // public
    // using in any where
    public int summer;
    public String som;

    // protected
    // using in same package and sub classes (using inherited classes)
    protected int id;
    protected String age;


    


}
